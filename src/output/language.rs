use crate::prelude::*;

pub(crate) fn localize_json_value(runtime: &Runtime, value: JsonValue) -> JsonValue {
    localize_json_value_for_language(runtime.output_language, value)
}

pub(crate) fn localize_json_value_for_language(
    output_language: OutputLanguage,
    value: JsonValue,
) -> JsonValue {
    if output_language == OutputLanguage::English {
        return value;
    }
    localize_json_value_for_key(output_language, None, value)
}

pub(crate) fn localize_json_key(output_language: OutputLanguage, key: &str) -> String {
    key_translation(output_language, key).unwrap_or_else(|| key.to_string())
}

fn localize_json_value_for_key(
    output_language: OutputLanguage,
    parent_key: Option<&str>,
    value: JsonValue,
) -> JsonValue {
    match value {
        JsonValue::JsonString(text) => JsonValue::JsonString(localize_json_string_value(
            output_language,
            parent_key,
            text,
        )),
        JsonValue::Array(items) => JsonValue::Array(
            items
                .into_iter()
                .map(|item| localize_json_value_for_key(output_language, parent_key, item))
                .collect::<Vec<_>>(),
        ),
        JsonValue::Object(fields) => {
            if matches!(parent_key, Some("instantiation")) {
                return JsonValue::Object(fields);
            }
            let mut localized_fields = Vec::new();
            for (key, field_value) in fields {
                let localized_value =
                    localize_json_value_for_key(output_language, Some(key.as_str()), field_value);
                localized_fields.push((
                    localize_json_key(output_language, key.as_str()),
                    localized_value,
                ));
            }
            JsonValue::Object(localized_fields)
        }
        JsonValue::Null | JsonValue::Bool(_) | JsonValue::Number(_) => value,
    }
}

fn localize_json_string_value(
    output_language: OutputLanguage,
    parent_key: Option<&str>,
    text: String,
) -> String {
    if output_language == OutputLanguage::English || should_preserve_string_value(parent_key) {
        return text;
    }
    text_translation(output_language, text.as_str()).unwrap_or(text)
}

fn should_preserve_string_value(parent_key: Option<&str>) -> bool {
    matches!(
        parent_key,
        Some("statement")
            | Some("fact")
            | Some("facts")
            | Some("inferred_facts")
            | Some("cited_statement")
            | Some("failed_goal")
            | Some("verify_what")
            | Some("goal")
            | Some("body")
            | Some("branches")
            | Some("requirements")
            | Some("assumptions")
            | Some("parameters")
            | Some("name")
            | Some("label")
            | Some("path")
            | Some("trace")
            | Some("runner")
            | Some("runner_version")
    )
}

fn key_translation(output_language: OutputLanguage, key: &str) -> Option<String> {
    match output_language {
        OutputLanguage::English => None,
        OutputLanguage::SimplifiedChinese => find_translation(ZH_KEYS, key),
        OutputLanguage::TraditionalChinese => find_translation(ZH_HANS_KEYS, key),
        OutputLanguage::Japanese => find_translation(JA_KEYS, key),
        OutputLanguage::Korean => find_translation(KO_KEYS, key),
        OutputLanguage::Spanish => find_translation(ES_KEYS, key),
        OutputLanguage::French => find_translation(FR_KEYS, key),
        OutputLanguage::German => find_translation(DE_KEYS, key),
        OutputLanguage::Portuguese => find_translation(PT_KEYS, key),
        OutputLanguage::Russian => find_translation(RU_KEYS, key),
        OutputLanguage::Arabic => find_translation(AR_KEYS, key),
        OutputLanguage::Hindi => find_translation(HI_KEYS, key),
        OutputLanguage::Vietnamese => find_translation(VI_KEYS, key),
        OutputLanguage::Indonesian => find_translation(ID_KEYS, key),
    }
}

fn text_translation(output_language: OutputLanguage, text: &str) -> Option<String> {
    let exact = match output_language {
        OutputLanguage::English => None,
        OutputLanguage::SimplifiedChinese => find_translation(ZH_TEXTS, text),
        OutputLanguage::TraditionalChinese => find_translation(ZH_HANS_TEXTS, text),
        OutputLanguage::Japanese => find_translation(JA_TEXTS, text),
        OutputLanguage::Korean => find_translation(KO_TEXTS, text),
        OutputLanguage::Spanish => find_translation(ES_TEXTS, text),
        OutputLanguage::French => find_translation(FR_TEXTS, text),
        OutputLanguage::German => find_translation(DE_TEXTS, text),
        OutputLanguage::Portuguese => find_translation(PT_TEXTS, text),
        OutputLanguage::Russian => find_translation(RU_TEXTS, text),
        OutputLanguage::Arabic => find_translation(AR_TEXTS, text),
        OutputLanguage::Hindi => find_translation(HI_TEXTS, text),
        OutputLanguage::Vietnamese => find_translation(VI_TEXTS, text),
        OutputLanguage::Indonesian => find_translation(ID_TEXTS, text),
    };
    if exact.is_some() {
        return exact;
    }

    if let Some(rest) = text.strip_prefix("cite ") {
        let localized_rest =
            text_translation(output_language, rest).unwrap_or_else(|| rest.to_string());
        return Some(cite_template(output_language, localized_rest.as_str()));
    }

    if let Some(rule) = text
        .strip_prefix("inferred by builtin rule `")
        .and_then(|s| s.strip_suffix('`'))
    {
        return Some(inferred_by_builtin_rule_template(output_language, rule));
    }

    if let Some(rule) = text
        .strip_prefix("inferred by infer rule `")
        .and_then(|s| s.strip_suffix('`'))
    {
        return Some(inferred_by_infer_rule_template(output_language, rule));
    }

    if let Some(name) = text
        .strip_prefix("prop with meaning `")
        .and_then(|s| s.strip_suffix("` (param constraints and definition clauses)"))
    {
        return Some(prop_meaning_template(output_language, name));
    }

    None
}

fn find_translation(entries: &[(&str, &str)], text: &str) -> Option<String> {
    for (source, target) in entries {
        if *source == text {
            return Some((*target).to_string());
        }
    }
    None
}

